package lyc.iping;

public class User {

	private String ID;
	private String username;
	private int HeadImageVersion;
	
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getHeadImageVersion() {
		return HeadImageVersion;
	}
	public void setHeadImageVersion(int headImageVersion) {
		HeadImageVersion = headImageVersion;
	}
	
}
